// Language switcher functionality
function changeLanguage(lang) {
  var frBtn = document.getElementById('lang-fr');
  var enBtn = document.getElementById('lang-en');
  
  if (frBtn && enBtn) {
    frBtn.classList.remove('active');
    enBtn.classList.remove('active');
    document.getElementById('lang-' + lang).classList.add('active');
  }
  
  localStorage.setItem('preferredLanguage', lang);
  
  if (lang === 'en') {
    translatePage();
  } else {
    location.reload();
  }
}

function translatePage() {
  // Translate all text nodes
  var walker = document.createTreeWalker(
    document.body,
    NodeFilter.SHOW_TEXT,
    {
      acceptNode: function(node) {
        var parent = node.parentElement;
        if (!parent || parent.tagName === 'SCRIPT' || parent.tagName === 'STYLE') {
          return NodeFilter.FILTER_REJECT;
        }
        if (node.textContent.trim().length > 0) {
          return NodeFilter.FILTER_ACCEPT;
        }
        return NodeFilter.FILTER_REJECT;
      }
    }
  );
  
  var textNodes = [];
  while (walker.nextNode()) {
    textNodes.push(walker.currentNode);
  }
  
  textNodes.forEach(function(node) {
    var originalText = node.textContent.trim();
    // Normalize whitespace: replace multiple spaces/newlines with single space
    var normalizedText = originalText.replace(/\s+/g, ' ').trim();
    
    // Try exact match first
    if (translations[originalText]) {
      node.textContent = translations[originalText];
    } 
    // Try normalized match
    else if (translations[normalizedText]) {
      node.textContent = translations[normalizedText];
    }
    // Try to find matching key with normalized comparison
    else {
      for (var key in translations) {
        var normalizedKey = key.replace(/\s+/g, ' ').trim();
        if (normalizedKey === normalizedText) {
          node.textContent = translations[key];
          break;
        }
      }
    }
  });
  
  // Translate attributes
  document.querySelectorAll('[title], [placeholder], [alt]').forEach(function(element) {
    if (element.hasAttribute('title')) {
      var title = element.getAttribute('title').trim();
      if (translations[title]) {
        element.setAttribute('title', translations[title]);
      }
    }
    if (element.hasAttribute('placeholder')) {
      var placeholder = element.getAttribute('placeholder').trim();
      if (translations[placeholder]) {
        element.setAttribute('placeholder', translations[placeholder]);
      }
    }
    if (element.hasAttribute('alt')) {
      var alt = element.getAttribute('alt').trim();
      if (translations[alt]) {
        element.setAttribute('alt', translations[alt]);
      }
    }
  });
  
  // Translate page title
  if (translations[document.title]) {
    document.title = translations[document.title];
  }
}

// Auto-translate on page load if English is preferred
document.addEventListener('DOMContentLoaded', function() {
  var urlParams = new URLSearchParams(window.location.search);
  var urlLang = urlParams.get('lang');
  var preferredLang = localStorage.getItem('preferredLanguage');
  var lang = urlLang || preferredLang;
  
  if (lang === 'en') {
    setTimeout(function() {
      var frBtn = document.getElementById('lang-fr');
      var enBtn = document.getElementById('lang-en');
      
      if (frBtn && enBtn) {
        frBtn.classList.remove('active');
        enBtn.classList.add('active');
      }
      
      translatePage();
    }, 100);
  }
});
